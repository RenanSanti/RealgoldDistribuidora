public class ComboItens {

    public String key;
    private String value;

    public ComboItens(String key, String value) {
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }

    public String toString() {
        return key + "-" + value;
    }
}
