        String stNumeroBandeja = "";
        //String stComboBoxNomeCliente = "";
        String stComboBoxIdCliente = "";
        String stObs = "";
        boolean stjCheckBoxLenteCasa = false;
        boolean stjCheckBoxPago = false;
        String stTipoLente = "";
        String idServico = "";
        Servico servico = null;
        Object comboSelecionadoCliente = null;
        Object comboSelecionadoTipoLente = null;
        servico = new Servico();
        SimpleDateFormat formatada = new SimpleDateFormat("yyyy-MM-dd");
        // SimpleDateFormat dataEntrada = new SimpleDateFormat("YY/MM/dd");
        //dataEntrada = new SimpleDateFormat("dd/MM/yyyy");

        //stDataEntrada = jTextFieldDataEntrada.getText();
        //stComboBoxNomeCliente = jComboBoxNomeCliente.getSelectedItem();
        //comboSelecionadoCliente = jComboBoxNomeCliente.getSelectedItem();
