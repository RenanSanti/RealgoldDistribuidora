    /**
     * Método responsável por carregar os campos que serão alterados
     */
    public void carregarCampos(Servico pServico) throws ParseException {
        ArrayList<Servico> listServico = DAOServico.consultarServicoComRestricao(pServico);
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy"); 

        if (!listServico.isEmpty()) {
            Servico servicoLista = listServico.get(0);

            String novaDataentrada = Util.formatadorData(servicoLista.getDiaEntradaServico().substring(0, 10));
            jTextFieldDataEntrada.setText(novaDataentrada);

            if (servicoLista.getDiaSaidaServico() != null) {
                jDataSaida.setDate(formato.parse(Util.formatadorData(servicoLista.getDiaSaidaServico().substring(0,10))));

            }
            jTexFiledIdServico.setText(servicoLista.getCodigoServico());

            // = servicoLista.getCodigoServico();
            jTextFieldNumeroBandeja.setText(servicoLista.getNumeroBandejaServico());
            jComboBoxNomeCliente.removeAllItems();
            jComboBoxNomeCliente.addItem(servicoLista.getNmclienteServico());

            jComboBoxTipoLente.removeAllItems();
            jComboBoxTipoLente.addItem(servicoLista.getDescricaoTipoLenteServico());

            if (servicoLista.getPagoServico().equals("Pago")) {
                jCheckBoxPago.setSelected(true);
            } else {
                jCheckBoxPago.setSelected(false);
            }
            //row[7] = "";//= listServico.get(i).getDiaSaidaServico();

            if (servicoLista.getIsLenteCasa()) {
                jCheckBoxLenteCasa.setSelected(true);
            } else {
                jCheckBoxLenteCasa.setSelected(false);
            }
            jTextAreaObservacao.setText(servicoLista.getObs());
        }

    }

