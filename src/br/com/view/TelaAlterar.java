        //comboSelecionadoTipoLente = jComboBoxTipoLente.getSelectedItem();
        //stTipoLente = ((ComboItens) comboSelecionadoTipoLente).key;
        stNumeroBandeja = jTextFieldNumeroBandeja.getText();
        idServico = jTexFiledIdServico.getText();
