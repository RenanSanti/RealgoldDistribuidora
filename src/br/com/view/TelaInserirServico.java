    /*
    Carrega o select de nome de Cliente
    */
    private void Show_jComboBoxNomeCliente() {
        ArrayList<Cliente> list = DAOCliente.consultarCliente();
        jComboBoxNomeCliente.removeAllItems();
        DefaultComboBoxModel model = new DefaultComboBoxModel();

        //Object[] row = new Object[14];
        for (int i = 0; i < list.size(); i++) {
            Cliente clienteLista = list.get(i);
            //jComboBoxNomeCliente.addItem(clienteLista.getNmNomeFantasia());
            model.addElement(new ComboItens(Integer.toString(clienteLista.getIdCliente()), clienteLista.getNmNomeFantasia()));

        }
        jComboBoxNomeCliente.setModel(model);
        
    }

    /*
        Carrega o select do tipo de lente
    */
    private void Show_jComboBoxTipoLente() {
        ArrayList<TipoLente> listTipoLente = DAOTipoLente.consultarTipoLente();
        jComboBoxTipoLente.removeAllItems();
        DefaultComboBoxModel model = new DefaultComboBoxModel();

        //Object[] row = new Object[14];
        for (int i = 0; i < listTipoLente.size(); i++) {
            TipoLente tipoLente = listTipoLente.get(i);
            //jComboBoxTipoLente.addItem(tipoLente.getDescricaoTipoLente());
            model.addElement(new ComboItens(Integer.toString(tipoLente.getIdTipoCliente()), tipoLente.getDescricaoTipoLente()));
        }
        jComboBoxTipoLente.setModel(model);
    }

