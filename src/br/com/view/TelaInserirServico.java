    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        //inserir serviço
        String stDataEntrada = "";
        String stNumeroBandeja = "";
        //String stComboBoxNomeCliente = "";
        String stComboBoxIdCliente = "";
        String stObs = "";
        String osCliente = "";
        boolean stjCheckBoxLenteCasa = false;
        boolean stjCheckBoxPago = false;
        String stTipoLente = "";
        int idServico = 0;
        Servico servico = null;
        boolean stjCheckBoxMontagem = false;
        boolean stjCheckBoxSurfacagem = false;
        
        Object comboSelecionadoCliente = null;
        Object comboSelecionadoTipoLente = null;
        servico = new Servico();
        // SimpleDateFormat dataEntrada = new SimpleDateFormat("YY/MM/dd");
        //dataEntrada = new SimpleDateFormat("dd/MM/yyyy");

        stDataEntrada = jTextFieldDataEntrada.getText();
        stNumeroBandeja = jTextFieldNumeroBandeja.getText();

        if (stNumeroBandeja.equals("")) {
            JOptionPane.showMessageDialog(null, "Favor preencher o número da bandeja!");

        }

        //stComboBoxNomeCliente = jComboBoxNomeCliente.getSelectedItem();
        comboSelecionadoCliente = jComboBoxNomeCliente.getSelectedItem();
        //stComboBoxNomeCliente = ((ComboItens)comboSelecionadoCliente).value;
        stComboBoxIdCliente = ((ComboItens) comboSelecionadoCliente).key;
        stjCheckBoxLenteCasa = jCheckBoxLenteCasa.isSelected();
        stjCheckBoxMontagem = jCheckBoxMontagem.isSelected();
        stjCheckBoxSurfacagem = jCheckBoxSurfacagem.isSelected();


        //stTipoLente = jComboBoxTipoLente.getSelectedItem();
        comboSelecionadoTipoLente = jComboBoxTipoLente.getSelectedItem();
        stTipoLente = ((ComboItens) comboSelecionadoTipoLente).key;

        stjCheckBoxPago = jCheckBoxPago.isSelected();
        stObs = jTextAreaObservacao.getText();
        osCliente = jTextOsCliente.getText();

        idServico = DAOServico.gerarSequencialIdServico();
        servico.setDiaEntradaServico(stDataEntrada);
        servico.setCodigoServico(Integer.toString(idServico));
        servico.setNumeroBandejaServico(stNumeroBandeja);
        servico.setIdClienteServico(stComboBoxIdCliente);
        servico.setIsLenteCasa(stjCheckBoxLenteCasa);
        servico.setDescricaoTipoLenteServico(stTipoLente);
        servico.setStatusServico("5");
        servico.setIsMontagem(stjCheckBoxMontagem);
        servico.setIsSurfacagem(stjCheckBoxSurfacagem);
        servico.setOsCliente(osCliente);
        if (stjCheckBoxPago) {
            servico.setPagoServico("Pago");
        } else {
            servico.setPagoServico("Não Pago");
        }
        servico.setObs(stObs);
        if (!stNumeroBandeja.equals("")) {
            DAOServico.inserirServico(servico);
            JOptionPane.showMessageDialog(null, "Sucesso!");
        }
        this.dispose();
        TelaInserirServico telaInserirServico = new TelaInserirServico();
        telaInserirServico.setVisible(true);
    }//GEN-LAST:event_jButtonInserirActionPerformed

