    private void jButtonLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLogarActionPerformed
        ArrayList<Usuario> arrayUsuario = DAOUsuario.consultarUsuario();
        boolean encontrou =  false;
        for (Usuario usuario : arrayUsuario) {
            String nmLoginDaTela = jTextLogin.getText();
            String nmSenhaDaTela = new String(jPassword.getPassword());
           if(nmLoginDaTela.equals(usuario.getNmLogin()) &&
                nmSenhaDaTela.equals(usuario.getNmSenha())   ){
               encontrou = true;            
            } 
        }
        
        if(encontrou){
            TelaPrincipal telaPrincipal = new TelaPrincipal(jTextLogin);
            telaPrincipal.setVisible(true);
            
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Login ou senha INCORRETO!");
        }
        
    }//GEN-LAST:event_jButtonLogarActionPerformed

